function hasbit(x, b)
  local p = b + 1
  return x % (p + p) >= p
end
function socreg_name(addr)
  local name = soc_names[addr]
  if name == nil then
    return string.format("%04x", addr)
  else
    return string.format("%04x_%s", addr, name)
  end
end
function socreg_read(addr)
  local data = soc_regs[addr]
  local name = socreg_name(addr)
  if data == nil then
    data = 0xdeadbeef
  end
  print(string.format("soc_read(%s): 0x%04x", name, data))
  return data
end
function socreg_write(addr, data)
  local name = socreg_name(addr)
  print(string.format("soc_write(%s, 0x%04x)", name, data))
  if addr == 0x0024 then
    -- RST_GLB_CTRL_0 will self-clear
    data = 0
  end
  if addr == 0x5cac then
    -- ITA_CTRL0 (RTL837X_TBL_CTRL) bits 0+1 will self-clear
    data = data & ~3
  end
  if addr == 0x53d4 then
    -- L2_TBL_FLUSH_CMD (RTL837x_L2_TBL_FLUSH_CTRL)
    data = 0
  end
  if addr == 0x0f60 then
    -- INDIRECT_ACCESS_CTRL (RTL837X_STAT_GET) bit 0 will self-clear
    data = data & ~1
  end
  soc_regs[addr] = data
end
function sfr_cache_ctrl(write, data)
  if write then
    if data & 4 then
      print("CACHE_CTRL.2=1")
      local adr = (spis_addr2 << 16) | (spis_addr1 << 8) | spis_addr0
      local unpadr = adr
      if adr > 0x4000 then
        local bank = ((adr - 0x4000) // 0xc000) + 1
        local ofs = ((adr - 0x4000) % 0xc000) + 0x4000
        unpadr = (bank << 16) | ofs
      end
      if spis_rcmd == 5 then
        spis_data0 = 0 -- Status: not busy
      elseif spis_rcmd == 0xbb then
        spis_data0 = emu.peek_rom(unpadr+0)
        spis_data1 = emu.peek_rom(unpadr+1)
        spis_data2 = emu.peek_rom(unpadr+2)
        spis_data3 = emu.peek_rom(unpadr+3)
      end
      local data = (spis_data3 << 24) | (spis_data2 << 16) | (spis_data1 << 8) | spis_data0
      print(string.format("SPI RCMD %02x @0x%06x(%06X)=%08x", spis_rcmd, adr, unpadr, data))
    end
  end
  return "CACHE_CTRL", 0
end
