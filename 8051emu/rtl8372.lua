function sfr_pedma_len(write, data)
  if write then
    local eaddr = emu.peek_sfr(0xb3) + (emu.peek_sfr(0xb4) << 8)
    local paddr = emu.peek_sfr(0xb5) + (emu.peek_sfr(0xb6) << 8)
    local pktlen = #arp_packet
    local pkt = {rx_seq, 0, 0x10, 0, 0, 0, 0, pktlen}
    local len = data * 8
    table_extend(pkt, arp_packet)
    print(string.format("soc_dma eaddr=%04x paddr=%04x cnt=%d\n", eaddr, paddr, len))
    if paddr >= 0x8000 then
      -- DMA write
      for i=1,len do
        local x = emu.peek_xram(eaddr+i-1)
        print(string.format("soc_dma_w: %04x: %02x \n", eaddr+i-1,x))
      end
    else
      -- DMA read
      for i=1,len do
        local x = pkt[i+paddr]
        if x == nil then
          x = 0xff
        end
        print(string.format("soc_dma_r: %04x = %02x \n", eaddr+i-1,x))
        emu.poke_xram(eaddr+i-1, x)
      end
    end
  end
  return "PEDMA_LEN", 0
end

